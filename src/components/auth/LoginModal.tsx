import React, { useState } from 'react';
import { X, Github, Mail, Loader2 } from 'lucide-react';
import { useAuth } from '../../contexts/AuthContext';
import { TRANSLATIONS } from '@/constants';
import { Language } from '@/types';

interface LoginModalProps {
    isOpen: boolean;
    onClose: () => void;
    lang?: Language;
}

export const LoginModal: React.FC<LoginModalProps> = ({ isOpen, onClose, lang = 'en' }) => {
    const { signInWithOAuth, signInWithEmail, signUpWithEmail, loading, error } = useAuth();
    const [mode, setMode] = useState<'login' | 'register'>('login');
    const [email, setEmail] = useState('');
    const [password, setPassword] = useState('');
    const [displayName, setDisplayName] = useState('');
    const t = TRANSLATIONS[lang];

    if (!isOpen) return null;

    const handleGoogleLogin = async () => {
        try {
            await signInWithOAuth('google');
        } catch (error) {
            console.error('Google login failed:', error);
        }
    };

    const handleGithubLogin = async () => {
        try {
            await signInWithOAuth('github');
        } catch (error) {
            console.error('GitHub login failed:', error);
        }
    };

    const handleSubmit = async (e: React.FormEvent) => {
        e.preventDefault();
        try {
            if (mode === 'login') {
                await signInWithEmail(email, password);
            } else {
                await signUpWithEmail(email, password, displayName);
            }
            onClose();
        } catch (error) {
            console.error('Email auth failed:', error);
        }
    };

    return (
        <div className="fixed inset-0 z-50 flex items-center justify-center p-4 bg-black/50 backdrop-blur-sm animate-in fade-in duration-200">
            <div className="relative w-full max-w-md bg-[#FDF8F5] rounded-3xl shadow-2xl overflow-hidden animate-in zoom-in-95 duration-200">
                {/* Close Button */}
                <button
                    onClick={onClose}
                    className="absolute top-4 right-4 p-2 rounded-full hover:bg-black/5 transition-colors z-10"
                >
                    <X className="w-6 h-6 text-gray-500" />
                </button>

                <div className="p-8 text-center">
                    {/* Logo */}
                    <div className="mb-6 flex justify-center">
                        <img src="/logo.png" alt="InstaGen Logo" className="w-16 h-16 object-contain" />
                    </div>

                    <h2 className="text-2xl font-bold text-[#1F2937] mb-2">
                        {t.loginTitle}
                    </h2>
                    <p className="text-[#9CA3AF] mb-6">
                        {t.loginSubtitle}
                    </p>

                    {/* Tabs */}
                    <div className="flex p-1 mb-6 bg-[#F5F5F4] rounded-xl">
                        <button
                            onClick={() => setMode('login')}
                            className={`flex-1 py-2 text-sm font-medium rounded-lg transition-all ${mode === 'login'
                                ? 'bg-white text-[#E76F51] shadow-sm'
                                : 'text-[#9CA3AF] hover:text-[#374151]'
                                }`}
                        >
                            {t.login}
                        </button>
                        <button
                            onClick={() => setMode('register')}
                            className={`flex-1 py-2 text-sm font-medium rounded-lg transition-all ${mode === 'register'
                                ? 'bg-white text-[#E76F51] shadow-sm'
                                : 'text-[#9CA3AF] hover:text-[#374151]'
                                }`}
                        >
                            {t.register}
                        </button>
                    </div>

                    {/* Email Form */}
                    <form onSubmit={handleSubmit} className="space-y-4 mb-6 text-left">
                        {mode === 'register' && (
                            <div>
                                <label className="block text-xs font-medium text-[#374151] mb-1 ml-1">{t.name}</label>
                                <input
                                    type="text"
                                    value={displayName}
                                    onChange={(e) => setDisplayName(e.target.value)}
                                    className="w-full px-4 py-3 bg-white border-2 border-[#E5E5E5] rounded-xl text-[#374151] placeholder:text-[#9CA3AF] focus:border-[#E76F51] focus:ring-2 focus:ring-[#E76F51]/20 transition-all outline-none"
                                    placeholder={t.name}
                                    required
                                />
                            </div>
                        )}
                        <div>
                            <label className="block text-xs font-medium text-[#374151] mb-1 ml-1">{t.email}</label>
                            <input
                                type="email"
                                value={email}
                                onChange={(e) => setEmail(e.target.value)}
                                className="w-full px-4 py-3 bg-white border-2 border-[#E5E5E5] rounded-xl text-[#374151] placeholder:text-[#9CA3AF] focus:border-[#E76F51] focus:ring-2 focus:ring-[#E76F51]/20 transition-all outline-none"
                                placeholder="name@example.com"
                                required
                            />
                        </div>
                        <div>
                            <label className="block text-xs font-medium text-[#374151] mb-1 ml-1">{t.password}</label>
                            <input
                                type="password"
                                value={password}
                                onChange={(e) => setPassword(e.target.value)}
                                className="w-full px-4 py-3 bg-white border-2 border-[#E5E5E5] rounded-xl text-[#374151] placeholder:text-[#9CA3AF] focus:border-[#E76F51] focus:ring-2 focus:ring-[#E76F51]/20 transition-all outline-none"
                                placeholder="••••••••"
                                required
                            />
                        </div>

                        {error && (
                            <div className="text-xs text-[#E63946] bg-[#FEF2F2] p-2 rounded-lg">
                                {error.message}
                            </div>
                        )}

                        <button
                            type="submit"
                            disabled={loading}
                            className="w-full py-3 bg-gradient-to-r from-[#E76F51] to-[#F4A261] text-white font-medium rounded-xl shadow-md hover:shadow-lg transform hover:scale-[1.02] transition-all duration-200 disabled:opacity-70 disabled:cursor-not-allowed flex items-center justify-center gap-2"
                        >
                            {loading && <Loader2 className="w-4 h-4 animate-spin" />}
                            {mode === 'login' ? t.login : t.register}
                        </button>
                    </form>

                    <div className="relative mb-6">
                        <div className="absolute inset-0 flex items-center">
                            <div className="w-full border-t border-[#E5E5E5]"></div>
                        </div>
                        <div className="relative flex justify-center text-xs uppercase">
                            <span className="bg-[#FDF8F5] px-2 text-[#9CA3AF]">{t.or}</span>
                        </div>
                    </div>

                    <div className="space-y-3">
                        <button
                            onClick={handleGoogleLogin}
                            className="w-full flex items-center justify-center gap-3 px-6 py-3 bg-white border-2 border-[#E5E5E5] rounded-xl text-[#374151] font-medium hover:border-[#E76F51] hover:text-[#E76F51] transition-all duration-200 transform hover:scale-[1.02]"
                        >
                            <svg className="w-5 h-5" viewBox="0 0 24 24">
                                <path
                                    d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                                    fill="#4285F4"
                                />
                                <path
                                    d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                                    fill="#34A853"
                                />
                                <path
                                    d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                                    fill="#FBBC05"
                                />
                                <path
                                    d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                                    fill="#EA4335"
                                />
                            </svg>
                            {t.googleLogin}
                        </button>

                        <button
                            onClick={handleGithubLogin}
                            className="w-full flex items-center justify-center gap-3 px-6 py-3 bg-[#24292F] text-white rounded-xl font-medium hover:bg-[#333] transition-all duration-200 transform hover:scale-[1.02] shadow-md"
                        >
                            <Github className="w-5 h-5" />
                            {t.githubLogin}
                        </button>
                    </div>

                    <div className="mt-8 text-xs text-[#9CA3AF]">
                        {t.agreeTo}
                        <a href="#" className="text-[#E76F51] hover:underline mx-1">{t.terms}</a>
                        {t.and}
                        <a href="#" className="text-[#E76F51] hover:underline mx-1">{t.privacy}</a>
                    </div>
                </div>
            </div>
        </div>
    );
};
